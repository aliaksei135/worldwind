package com.cfar.swim.worldwind.planning;

import java.time.ZonedDateTime;

public class CostInterval extends TimeInterval {
	
	private int cost = 1;
	
	public CostInterval(ZonedDateTime time) {
		super(time);
	}
	
	public CostInterval(ZonedDateTime start, ZonedDateTime end) {
		super(start, end);
	}
	
	public int getCost() {
		return this.cost;
	}

	public void setCost(int cost) {
		this.cost = cost;
	}

	public int getWeightedCost() {
		// TODO: costs could be weighted or a weight factor applied explicitly
		return this.cost;
	}
	
}
